      h2 1.5 Reconocimiento, medición, presentación y revelación
    p <b>Reconocimiento:</b> al momento de reconocer un bien como propiedad, planta y equipo se debe identificar la definición del activo, es decir, que a futuro genere beneficios económicos de la utilización de este dentro de la empresa, además, debe cumplir con la condición de que el precio de ese activo es fiable. 
